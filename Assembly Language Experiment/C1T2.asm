.486
DATAS SEGMENT USE16
NUM DB -19,+28,37,-46,+55,61,-74,+255
CT DB 0
MEG1 DB 'The number of negative numbers is:',?,0DH, 0AH,'$'
MEG2 DB 0AH,'MAX=',?,0DH, 0AH,'$'
MEG3 DB 0AH,'MIN=',?,0DH, 0AH,'$'
DATAS ENDS

CODES SEGMENT USE16
ASSUME CS:CODES,DS:DATAS
BEG:
MOV AX,DATAS
MOV DS,AX
MOV SI,OFFSET NUM
MOV CX,8
MOV DL,0
MOV BL,0

CIR: 
CMP BYTE PTR [SI],0
JG NEXT
INC DL;COUNT NEGTIVE NUM
CMP BYTE PTR[SI],BL
JLE GETMIN
	
NEXT:
	CMP BYTE PTR[SI],BH
	JGE GETMAX
	INC SI
	LOOP CIR
	ADD DL,30H
	MOV AH,9
	MOV MEG1+34,DL
	LEA DX,OFFSET MEG1
	INT 21H
	MOV AH,9
	LEA DX,OFFSET MEG2
	INT 21H
	MOV CX,8
	
SHOWMAX:
	MOV DL,'0'
    MOV AH,2
    ROL BH,1
    ADC DL,0
    INT 21H
    LOOP  SHOWMAX 
    MOV AH,2
    MOV DL,'B'
    MOV AH,2
    INT 21H 
    MOV AH,9           
    MOV DX,OFFSET MEG3
    INT 21H
    MOV CX,8

SHOWMIN:
	MOV DL,'0'
	MOV AH,2
	ROL BL,1
    ADC DL,0
    INT 21H
    LOOP  SHOWMIN  
    MOV DL,'B'
    MOV AH,2
    INT 21H
    MOV AH, 4CH
    INT 21H
    
GETMIN:
	MOV BL,BYTE PTR[SI]
	INC SI
	LOOP CIR
	
GETMAX:
	MOV BH,BYTE PTR[SI]
	INC SI
	LOOP CIR

CODES ENDS

END BEG
